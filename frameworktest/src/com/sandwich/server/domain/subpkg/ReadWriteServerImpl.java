package com.sandwich.server.domain.subpkg;

import com.sandwich.server.BusObj;

/**
 * The BusObj w/ no DTO proxy interface
 * @see ReadWriteExample 
 */
public class ReadWriteServerImpl implements ReadWriteExample, BusObj{
	private String read = 	"read";
	private String write = 	"write";

	@Override
	public String getRead() {
		return read;
	}

	@Override
	public String getWrite() {
		return write;
	}

	@Override
	public void setWrite(String write) {
		this.write = write;
	}
}
